#include <stdio.h>

int main(void) {
    // 화면을 지우고 커서를 왼쪽 상단(1,1)으로 이동시키는 ANSI 코드
    printf("\033[2J\033[1;1H"
        // 첫 번째 줄 그리기
        "######"
        // 커서를 다음 줄(2행) 첫 번째 위치로 이동
        "\033[2;1H"
        "#"
        // 커서를 현재 줄의 13번째 위치로 이동 후 # 출력
        "\033[2;13H"
        "#"
        // 3번째 줄 그리기
        "\033[3;1H"
        "#"
        "\033[3;13H"
        "#"
        // 4번째 줄 그리기
        "\033[4;1H"
        "#"
        "\033[4;13H"
        "#"
        // 5번째 줄 그리기
        "\033[5;1H"
        "######"
        // 마지막으로 커서를 화면 맨 아래로 이동하여 터미널 모양 유지
        "\033[6;1H");

    return 0;
}